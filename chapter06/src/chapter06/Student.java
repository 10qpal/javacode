package chapter06;

public class Student {
	String studentId;
	String studentName;
	int korean;
	int english;
	int math;
	
	public Student(String studentId, String studentName, int korean, int english, int math) {
		super();
		this.studentId = studentId;
		this.studentName = studentName;
		this.korean = korean;
		this.english = english;
		this.math = math;
	}
}
